import math

pi = math.pi

#some examples of math functions using pi variable
def math_functions():
    #ceil
    print(math.ceil(pi))
    #floor
    print(math.floor(pi))
    #fabs
    print(math.fabs(pi))
    #factorial
    print(math.factorial(5))
    #fmod
    print(math.fmod(5, 2))
    #frexp
    print(math.frexp(pi))
    #fsum
    print(math.fsum([1, 2, 3]))
    #isinf
    print(math.isinf(pi))
    #isnan
    print(math.isnan(pi))
    #ldexp
    print(math.ldexp(pi, 2))
    #modf
    print(math.modf(pi))
    #trunc
    print(math.trunc(pi))
    #exp
    print(math.exp(pi))
    #expm1
    print(math.expm1(pi))
    #log
    print(math.log(pi))
    #log10
    print(math.log10(pi))
    #log1p
    print(math.log1p(pi))
    #pow
    print(math.pow(pi, 2))
    #sqrt
    print(math.sqrt(pi))
    #acos
    print(math.acos(pi))
    #asin
    print(math.asin(pi))
    #atan
    print(math.atan(pi))
    #atan2
    print(math.atan2(pi, 2))
    #cos
    print(math.cos(pi))
    #hypot
    print(math.hypot(pi, 2))
    #sin
    print(math.sin(pi))
    #tan
    print(math.tan(pi))
    #degrees
    print(math.degrees(pi))
    #radians
    print(math.radians(pi))
    #acosh
    print(math.acosh(pi))
    #asinh
    print(math.asinh(pi))
    #atanh
    print(math.atanh(pi))
    #cosh
    print(math.cosh(pi))
    #sinh
    print(math.sinh(pi))
    #tanh
    print(math.tanh(pi))
    #erf
    print(math.erf(pi))
    #erfc
    print(math.erfc(pi))
    #gamma
    print(math.gamma(pi))
    #lgamma
    print(math.lgamma(pi))
    #isfinite
    print(math.isfinite(pi))
    #isinf
    print(math.isinf(pi))
    #isnan
    print(math.isnan(pi))